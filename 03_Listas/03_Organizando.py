#Ordenação Permanente método sort()
cars = ['bmw','audi','toyota','subaru']
print(cars)
cars.sort() #ordened permatily
print(cars)
cars.sort(reverse=True) #ordened permatily
print(cars)

#Ordenação temporaria método sorted()
cars = ['bmw','audi','toyota','subaru']
print(cars)
cars.sorted() #ordened temporary
print(cars)
cars.sorted(reverse=True) #ordened temporary
print(cars)

#Exibisão em ordem inversa método revers()
cars = ['bmw','audi','toyota','subaru']
print(cars)
cars.reverse() #ordened temporary
print(cars)
cars.reverse() #ordened temporary
print(cars)

#Tamanho da lista método len()
print(len(cars))